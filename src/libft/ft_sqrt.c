int	ft_sqrt(int nb)
{
	int res;

	res = 0;
	if (nb >= 0 && nb <= 2147483647)
	{
		while (res * res != nb)
		{
			res++;
			if (res * res > nb)
			{
				res = 0;
				break ;
			}
		}
	}
	else if (nb < 0)
		res = 0;
	return (res);
}
